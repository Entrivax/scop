#include "app.h"

t_mat4			mat4_translate(t_mat4 m, t_vec4 v)
{
	t_mat4	res;

	res = m;
	res.m[3] += v.x;
	res.m[3 + 4] += v.y;
	res.m[3 + 4 + 4] += v.z;
	res.m[3 + 4 + 4 + 4] += v.w;
	return (res);
}
